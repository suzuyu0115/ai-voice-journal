async function seed() {
  // RLS 有効化後は匿名認証が必要
  console.log('匿名サインイン中...');
  const { error: authError } = await supabase.auth.signInAnonymously();
  if (authError) {
    console.error('認証失敗:', authError.message);
    process.exit(1);
  }
  console.log('サインイン完了');

  console.log(`\nシードデータ投入開始（${entries.length} 件 / 6/1〜6/22）...`);

  const { data, error } = await supabase
    .from('diary_entries')
    .insert(entries)
    .select('id, title, created_at');

  if (error) {
    console.error('投入失敗:', error.message);
    process.exit(1);
  }

  console.log('投入完了:');
  (data ?? []).forEach((row: { id: string; title: string; created_at: string }, i: number) => {
    const d = new Date(row.created_at);
    const label = `6/${d.getDate()} ${String(d.getHours()).padStart(2, '0')}:${String(d.getMinutes()).padStart(2, '0')}`;
    console.log(`  ${String(i + 1).padStart(2, '0')}. [${label}] ${row.title}`);
  });
}

seed();
