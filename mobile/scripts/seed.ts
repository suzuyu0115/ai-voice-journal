// JST の日時を UTC ISO 文字列に変換（JST = UTC+9）
function jst(day: number, hour: number, minute = 0): string {
  return new Date(
    `2026-06-${String(day).padStart(2, '0')}T${String(hour).padStart(2, '0')}:${String(minute).padStart(2, '0')}:00+09:00`
  ).toISOString();
}

