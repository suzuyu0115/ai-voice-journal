async function seed() {
  console.log(`シードデータ投入開始（${entries.length} 件）...`);

  const { data, error } = await supabase
    .from('diary_entries')
    .insert(entries)
    .select('id, title');

  if (error) {
    console.error('投入失敗:', error.message);
    process.exit(1);
  }

  console.log('投入完了:');
  (data ?? []).forEach((row: { id: string; title: string }, i: number) => {
    console.log(`  ${i + 1}. [${row.id}] ${row.title}`);
  });
}

seed();
